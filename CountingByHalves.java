public class CountingByHalves {
    public static void main(String[] args) {
        // Display title and divider of table
        System.out.println("x");
        System.out.println("------");
        
        // Begin counting from -10 and go up to 10 by increments of 0.5
        for (double x = -10; x <= 10; x += 0.5) {
            System.out.println(x);
        }
            
    }
}